#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int h;

	if (n < 0)
	{
		ft_putchar('-');
		h = n * (-1);
	}
	else
		h = n;
	if (h > 9)
		ft_putnbr(h / 10);
	ft_putchar(h % 10 + '0');
}
