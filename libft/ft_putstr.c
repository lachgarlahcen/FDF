#include "libft.h"

void	ft_putstr(char const *s)
{
	int i;

	i = 0;
	if (s != NULL && *s != '\0')
	{
		while (s[i] != '\0' && s != NULL)
		{
			write(1, &s[i], 1);
			i++;
		}
	}
}
