#include "libft.h"

void	ft_sort_int_table(int *tab, int size)
{
	int i;

	i = 0;
	while (i < size - 1)
	{
		if (tab[i] > tab[i + 1])
		{
			ft_intswap(&tab[i], &tab[i + 1]);
			i = 0;
		}
		else
			i++;
	}
}
